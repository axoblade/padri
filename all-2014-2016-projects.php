              <div class="entry-content">
                <p>
                  The Government of Uganda (GoU), through the National Planning Authority (NPA) commissioned PADRI to develop a Local Government (LG) development Issues paper for the National Development Plan II 2015/16-2019/20 to guide the implementation of the Uganda Vision 2040, of "A transformed Ugandan Society from a peasant to a modern and prosperous country within 30 years". PADRI conducted country-wide consultations with LG leaders at district, Municipality and sub-county levels (including both political and technical leadership); and other stakeholders at national level, to elicit their views on the priority development issues for the NDP II. The Key issues that were explored include: Improving the functionality of the LGs for effective service delivery, by  improving the LG revenue base; improving the capacity and levels of staffing; strengthening the planning function and monitoring and evaluation of LGs; and addressing governance issues - particularly   addressing the high levels of corruption; and improving the capacity and competencies of the political leadership (mostly councillors) at LG level, to perform their policy-making and oversight functions. Other issues were: Improving Community mobilization for development; promoting the Local Economic Development (LED) and promoting comprehensive physical planning for urban development. The study was conducted between March and October 2014 and funded by GoU.
