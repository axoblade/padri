              <h2 class="entry-title">
                <a>Measuring the socioeconomic impact of orthotic equipment on disabled peoples’ lives in Uganda</a>
              </h2>

              <div class="entry-meta">
                <ul>
                  <li class="d-flex align-items-center"><i class="icofont-user"></i> PADRI in collaboration with researchers from the School of Economics Nottingham University UK and medical practitioners from Nottingham University Hospital NHS Trust</li>
                  <li class="d-flex align-items-center"><i class="icofont-wall-clock"></i> <time datetime="2020-01-01">June 2013</time></li>
                  <li class="d-flex align-items-center"><i class="icofont-comment"></i>Uganda</li>
                </ul>
              </div>

              <div class="entry-content">
                <p>
                  PADRI in collaboration with researchers from the School of Economics Nottingham University UK and medical practitioners from Nottingham University Hospital NHS Trust participated in a randomized trial to evaluate the impact of providing orthotic equipment on life outcomes (economic and physical independence and wellbeing) for disabled adults in Uganda. The study was conducted in two phases. In the first phase, 800 people with physical disability (of the lower limb) were assessed and interviewed. Of these 400 were randomly assigned to the ‘intervention’ group and were allocated and assistive device depending on their condition, while 400 were assigned to the control group 9those who received no assistive device in the first phase). In the second phase,   all the 800 were reassessed and re-interviewed to establish the impact of the intervention and the same time, those in the control group were allocated an appropriate assistive device. The impact of the equipment on physical independence and the quality of life of individuals, changes in skills, employment opportunities and income were measured based on the data collected from the intervention and control group in the 2 phases of the study. In addition to a survey of the people with disability (PWDs) who participated in the study, focus group discussions (FGDs) were held with community members to understand the community attitudes towards PWDs. The project results provide academics, policy makers, the Government of Uganda and international donors with important evidence and policy options for improving the lives of persons with physical disability in Uganda.  Phase 1 fieldwork was conducted in June 2012 and phase 2 in June 2013.  The National Union of Disabled Persons (NUDIPU) and Uganda National Action for Persons with Disability (UNPD) were the local institutions which participated in the study.
                </p>
