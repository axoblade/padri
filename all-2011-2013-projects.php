              <h2 class="entry-title">
                <a >The Cost of motorcycle Accidents in Uganda</a>
              </h2>

              <div class="entry-meta">
                <ul>
                  <li class="d-flex align-items-center"><i class="icofont-user"></i>PADRI</li>
                  <li class="d-flex align-items-center"><i class="icofont-wall-clock"></i> <time datetime="2020-01-01">Nov 2013 - June 2014</time></li>
                  <li class="d-flex align-items-center"><i class="icofont-comment"></i>Uganda</li>
                </ul>
              </div>

              <div class="entry-content">
                <p>
                  PADRI researchers, in collaboration with research colleagues from, the Faculty of Business & Administration Uganda Christian University undertook a study to explore the costs of motorcycle accidents and the pain, grief and sufferings of the motorcycle accident victims using a multi-method approach. The specific objectives of this study were to estimate the direct and indirect costs of motorcycle accidents; establish the pain, grief and suffering of motorcycle accident victims; and draw policy recommendations for improving road safety in Uganda. The data was obtained from multiple sources, including survey of 1600 motorcycle riders (popularly known as boda boda) in Kawempe and Central divisions  Kampala Capital City Authority, interviews with accident victims and their immediate family members,  traffic police records, hospitals and national statistics. Data from the survey of motorcyclists and from the qualitative interviews were used to estimate and describe the tangible and intangible costs of motorcycle accidents- both for fatality and severe injuries. The Willingness-to-pay (WTP) approach was used to estimate the value that boda boda riders would pay for reducing the risk of loss of life based on Contingent Valuation (CV) method.  The study also explored the key coping mechanisms adopted by the Boda-boda riders amidst the challenges the riders face when they suffer motorcycle accidents. The unique contribution of this approach is that it offers an in-depth investigation of the pain, grief and suffering by drawing on rich qualitative interviews conducted with the Boda-boda riders who have suffered a motorcycle accident and the family members of the Boda-boda riders who died because of motorcycle accidents. The estimates showed that it costs approximately UGX 7 million (about US 2300 in 2014 prices) to treat a boda boda accident victim who is severely injured. This study was conducted between Nov 2013 and June 2014 and funded by CrossRoads, UK.

