              <div class="entry-content">
                <p>
                  PADRI staff conducted a process evaluation of the distribution of Long Lasting Insect-treated Nets (LLIN), under the Stop Malaria Project by Malaria Consortium in Uganda.  The process evaluation identified the strengths and challenges to of the LLIN distribution during ANC visits at health centres to inform future programmes on the distribution of LLIN through health facilities in Uganda. Fieldwork was conducted in Kayunga, Hoima, Ssembabule, Mityana, Kumi and Serere district- covering a total of 38 health facilities. The evaluation was done through a health facility survey, and in-depth interviews with the in-charge and /administrators of the health facilities, the district health officer (DHO) and other members of the district health team (DHT), district Biostatisticians  and officials at the Ministry of Health, National Malaria control Program (NMCP).  The process evaluation focused at the LLIN distribution chain, including coordination, supervision, record keeping and reporting, LLIN distribution to beneficiaries, behavioural change communication; and beneficiary assessment. The evaluation was conducted from May - August 2013 and was funded by Malaria Consortium-Uganda.
