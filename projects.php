              <h2 class="entry-title">
                <a >Empowering Vulnerable Women in Eastern African through Innovative Savings Groups</a>
              </h2>

              <div class="entry-meta">
                <ul>
                  <li class="d-flex align-items-center"><i class="icofont-user"></i>Fred Matovu, PhD</li>
                  <li class="d-flex align-items-center"><i class="icofont-wall-clock"></i> <time datetime="2020-01-01">Current</time></li>
                  <li class="d-flex align-items-center"><i class="icofont-comment"></i>Burundi, Kenya and Uganda</li>
                </ul>
              </div>

              <div class="entry-content">
                <p>
                  <strong>The International Development Research Centre (IDRC), Canada</strong> is supporting researchers in Uganda, Kenya and Burundi to undertake a study to explore how innovative Savings Schemes can lead to economic empowerment of vulnerable women in Eastern African Countries. The lack of access to affordable financial services is one of the major bottlenecks to starting and growing income generating activities among women in east Africa. This could be attributed to the limited number of financial service providers that target marginalized women in rural areas and in informal settlements. Financial markets are also established on a challenging environment complicated by security concerns, economic and political instability, minimal infrastructure, a dispersed population, and limited economic activities. These factors among others keep the majority of the women in Africa unable to access financial services. As such, they are unable to turn their business ideas into reality and many of them remain economically unproductive. Innovative savings schemes do provide an opportunity for vulnerable women to achieve economic and social empowerment, by enabling them make regular savings to finance investments in business, asset acquisition and household expenditures. There are networks of saving groups for women within east African region but there is a paucity of evidence on how they have impacted on the social and economic empowerment of the women, and how these schemes enable women to cope with economic shocks. The overall objective of this research project is to evaluate theimpact of innovative savings schemes and the ways in which they have economically empowered women in E. Africa and enabled them to cope with shocks and vulnerability. Specifically, the research will: <br>
                  <i>(i) Document the most promising and innovative financial schemes to women empowerment within the target areas of the three countries; <br>
                  (ii) Investigate the motivating factors for joining and remaining in Savings Groups (SGs), and how decision-making power relations influence women’s participation in SGs;<br> 
                  (iii) Examine the effects of SGs on Women’s usage of financial services, business activity, income, empowerment, consumption, and their ability to cope with economic shocks;<br> 
                  (iv) Evaluate the differences in penetration, operation, and impact of SGs between fragile and stable states; <br>
                  and (v) recommend practical policy relevant results and directions on how to support and scale-up savings schemes in Africa. </i></p>
                  <p><strong>The Project used a quasi-experimental survey design involving three categories of households:</strong><br>
                  (1) women who belong to an innovative savings group network (World Concern (WC) in Kenya and Uganda; and Five Talents (5T) in Burundi) 
                  <br>(2) women who belong to other savings group; and 
                  <br>(3) women do not belong to any savings group.<br>
                  The impact of savings groups on women’s welfare is assessed through a comparison of the women’s circumstances at baseline and at endline, 12 months after the baseline. It is hypothesized that innovative savings groups significantly improve the welfare of women compared to belonging to a regular savings group; and that when women participate is savings schemes of any kind, their welfare is much better than those who are not enrolled into any savings groups. A difference-in-difference analysis between women in the WC and 5T savings group Network and those in any other savings group will be done to assess the effect of the innovative savings groups on women’s welfare. Likewise the group of women in WC/5T and those in regular savings groups will be compared with women not belonging to any savings group to assess the impact of savings groups on women’s welfare more generally.  The women (household) survey is powered to capture a range of control variables.
                </p>
