              <h2 class="entry-title">
                <a>The Uganda Cost of Living with Disability Survey (UCLDS)</a>
              </h2>

              <div class="entry-meta">
                <ul>
                  <li class="d-flex align-items-center"><i class="icofont-user"></i> PADRI in partnership with Development Pathways</li>
                  <li class="d-flex align-items-center"><i class="icofont-wall-clock"></i><time datetime="2020-01-01">2019</time></li>
                  <li class="d-flex align-items-center"><i class="icofont-comment"></i>Uganda</li>
                </ul>
              </div>

              <div class="entry-content">
                <p>
                  PADRI, in partnership with Development Pathways conducted the fieldwork for the Uganda Cost of Living with Disability Survey (UCLDS) commissioned by the Ministry of Gender Labour and Social Development and conducted by Development (MGLSD). The Survey was the first of its kind to provide a situational analysis of disability in Uganda. It aimed at collecting information needed to estimate the ‘cost of disability’ and resource needs of people with disabilities relative to the population as a whole. This information is critical for determining adequate levels of income support and for prioritizing service provision for people with disabilities. PADRI was directly involved in managing the fieldwork activities, which included over 4000 households from 76 districts across all the regions in Uganda.
                  <br>The households included those with a member with a functional difficulty (50%) and those with no member experiencing any form of functional difficulty (50%). The sample was randomly determined through a rigorous process which involved screening all households in each of the 129 enumeration areas selected by UBOS, based on the 2014 National Population and Housing Census sampling frame, to determine households with a member with functional difficulty and those without any member with a functional difficulty.  Using the World Bank Survey-solutions program, households in each category were then selected for interviews. The data collection activities took place from September to November 2019. The findings of this situational analysis of the will be used by Government for policy development and programming for appropriate interventions for people living with disabilities in Uganda.<br> 
                  More details can be obtained from the contact person for this survey at MGLSD <strong>(Ms. Emily Ajiambo: email: ajiamboemily@gmail.com)</strong>
                </p>
